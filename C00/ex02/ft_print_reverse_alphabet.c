#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char	alp;

	alp = 122;
	while (alp >= 97)
	{
		ft_putchar(alp);
		alp--;
	}
}
